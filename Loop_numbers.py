"""
Write a loop that counts from 0 to 10 using a while loop
"""

def main():

	# Using while loop
	print("This using while loop")
	num = 0 
	while num < 11:
		print(num)
		num += 1

	# Using for loop
	print("\nThis using for loop")
	for a in range(0, 11):
		print(a)

if __name__ == "__main__":
	main()